public class for_demo {
	public static void main(String[] args) {
		int i = 0;
		while (i < 5) {
			System.out.println(i); // repeated work
			i++;
		}
System.out.println("=============");
		for (int c = 0; c < 5; c++) {
//			repeated work
			System.out.println(c);
		}
		for(;;) {
			System.out.println("Sdfgsdfg");
		}

	}

}
